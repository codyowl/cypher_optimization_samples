# helper function for executing queries
def run_query_list_on_tx(tx, queries):
    start_time = time.time()
    results = []
    for query in queries:
        print("Executing query: {0}".format(query))
        result = tx.run(query)
        results.append(result)
    end_time = time.time()
    print(f"Executing queries took {end_time - start_time} seconds")
    return results

'''
first approach with apoc single query
'''
def run_query_list_on_tx_with_apoc(tx, queries):
    start_time = time.time()

    # a single apoc query to run all the queries
    apoc_query = "UNWIND $queries AS query CALL apoc.cypher.run(query, {}) YIELD value RETURN count(value)"
    
    # running apoc o process all the queries
    result = tx.run(apoc_query, queries=queries)

    end_time = time.time()
    print(f"Executing queries with APOC took {end_time - start_time} seconds")
    return result

'''
second approach using a peroidic iteraite
'''
def run_query_list_on_tx_with_periodic(tx, queries):
    start_time = time.time()
    
    periodic_query = """
    CALL apoc.periodic.iterate(
      "UNWIND $queries AS query RETURN query",
      "CALL apoc.cypher.run(query, {}) YIELD value RETURN value",
      {batchSize: 100}
    ) YIELD batches, total
    RETURN batches, total
    """
    result = tx.run(periodic_query, queries=queries)
    
    end_time = time.time()
    print(f"Executing queries with `apoc.periodic.iterate` took {end_time - start_time} seconds")
    return result


